function tile_get_id(c)
{
	switch(c)
	{
		case "#":
			return 1;

		case "<":
			return 2;

		case ">":
			return 3;

		case "/":
			return 6;

		case "!":
			return 7;

		case "-":
			return 8;

		case "+":
			return 9;
	}

	return 0;
}

