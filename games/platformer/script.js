var player =
{
	startX: 0,
	startY: 0,
	x: 0,
	y: 0,
	w: 2 * world.tileSize,
	h: 2 * world.tileSize,
	vX: 0,
	vY: 0,
	speed: 4.5,
	floor: false,
	dead: false,
	die: function()
	{
		player.dead = true;
	},
	update: function(keys)
	{
		if(keys[keyBinding.jump])
		{
			if(this.floor)
			{
				this.floor = false;
				this.vY = -this.speed * 2;
			}
		}

		if(keys[keyBinding.moveRight])
		{
			if(this.vX < this.speed)
			{
				this.vX++;
			}
		}

		if(keys[keyBinding.moveLeft])
		{
			if(this.vX > -this.speed)
			{
				this.vX--;
			}
		}

		this.vX *= world.friction;
		this.vY += world.gravity;
		this.x += player.vX;
		this.y += player.vY;
	},
	draw: function(context)
	{
		/* add sprites */
		//context.fillStyle = "black";
		//context.fillRect(this.x, this.y, this.w, this.h);
		context.drawImage(img_player, this.x, this.y, this.w, this.h);
	}
};

