function startLevel(i)
{
	var x, y, rX, rY;
	if(world.levelId >= numLevels)
	{
		world.win = true;
		return;
	}

	world.level = levels[i];
	for(y = 0; y < world.levelHeight; y++)
	{
		for(x = 0; x < world.levelWidth; x++)
		{
			rX = x * world.tileSize;
			rY = y * world.tileSize;
			if(world.level[y][x] == "-")
			{
				player.startX = player.x = rX + world.tileSize / 2;
				player.startY = player.y = rY + world.tileSize;
				return;
			}
		}
	}
}

function randomInt(min, max)
{
	return Math.round(min + (Math.random() * max));
}

function collides(rect0, rect1, change)
{
	var vX = (rect0.x + (rect0.w / 2)) - (rect1.x + (rect1.w / 2));
	var vY = (rect0.y + (rect0.h / 2)) - (rect1.y + (rect1.h / 2));
	var hWidths = (rect0.w / 2) + (rect1.w / 2);
	var hHeights = (rect0.h / 2) + (rect1.h / 2);
	var res = null;
	if(Math.abs(vX) < hWidths && Math.abs(vY) < hHeights)
	{
		var oX = hWidths - Math.abs(vX)
		var oY = hHeights - Math.abs(vY);
		if(change)
		{
			if(oX >= oY)
			{
				if(vY > 0)
				{
					res = side.top;
					rect0.y += oY;
					rect0.vY *= -1;
				}
				else
				{
					res = side.bottom;
					rect0.y -= oY;
					rect0.floor = true;
					rect0.vY = 0;
				}
			}
			else
			{
				if(vX > 0)
				{
					res = side.left;
					rect0.x += oX;
				}
				else
				{
					res = side.right;
					rect0.x -= oX;
				}

				rect0.vX = 0;
			}
		}
		else
		{
			if(oX >= oY)
			{
				if(vY > 0)
				{
					res = side.top;
				}
				else
				{
					res = side.bottom;
				}
			}
			else
			{
				if(vX > 0)
				{
					res = side.left;
				}
				else
				{
					res = side.right;
				}
			}
		}
	}

	return res;
}

window.onload = function()
{
	var canvas = document.createElement("canvas");
	canvas.width = world.levelWidth * world.tileSize;
	canvas.height = world.levelHeight * world.tileSize;
	canvas.innerHTML = "Canvas not supported";
	document.body.appendChild(canvas);

	var context = canvas.getContext("2d");

	var animate =
		window.requestAnimationFrame || window.mozRequestAnimationFrame ||
		window.webkitRequestAnimationFrame || window.msRequestAnimationFrame;

	startLevel(0);

	function loop()
	{
		var x, y, rX, rY;


		if(world.win == true)
		{
			context.textAlign = "center";
			context.textBaseLine = "middle";
			context.fillStyle = "black";
			context.font = "100px Arial";
			context.fillText("YOU WIN!", canvas.width / 2, canvas.height / 2);
			return;
		}

		animate(loop);
		player.update(keys);

		/* Collision Loop */
		for(y = 0; y < world.levelHeight; y++)
		{
			for(x = 0; x < world.levelWidth; x++)
			{
				rX = x * world.tileSize;
				rY = y * world.tileSize;
				var curTile = tile_get_id(world.level[y][x]);
				if(curTile > 0)
				{
					var prop = tiles[curTile - 1];
					var hasFn = typeof prop.oncollision === "function";
					var isSolid = !(typeof prop.solid === "undefined" || !prop.solid);
					if(prop.solid || hasFn)
					{
						var box = { x: rX, y: rY, w: world.tileSize, h: world.tileSize };
						var dir = collides(player, box, isSolid);
						if(hasFn && dir != null)
						{
							prop.oncollision(dir);
						}
					}
				}
			}
		}

		/* Render Loop */
		//context.fillStyle = "#333333";
		//context.fillRect(0, 0, canvas.width, canvas.height);
		context.drawImage(img_bg, 0, 0, canvas.width, canvas.height);
		for(y = 0; y < world.levelHeight; y++)
		{
			for(x = 0; x < world.levelWidth; x++)
			{
				rX = x * world.tileSize;
				rY = y * world.tileSize;
				var curTile = tile_get_id(world.level[y][x]);
				if(curTile > 0)
				{
					var prop = tiles[curTile - 1];
					prop.draw(context, rX, rY);
				}
			}
		}

		player.draw(context);
		if(player.dead)
		{
			keys = [];
			alert("You Died!");
			player.dead = false;
			player.vX = 0;
			player.vY = 0;
			player.x = player.startX;
			player.y = player.startY;
		}

	}

	animate(loop);
};

